# syntax =>
# for item in iterable_object       (iterable object = collection of item / a list of numbers / a range / a string of charachters)
#                                   (item is a new variable that can be called whenever you want)

for number in range(1,8):
    print(number)
print(" ")

for x in range(1,10):
    print(x*x)
print(" ")

for letter in "coffee":
    print(letter)
print(" ")

for number in range(1,10,2):
    print(number)
print(" ")

for number in range(7,0,-1):
    print(number)
print(" ")






