# 1) using for

number = [1, 2, 3, 4]
for num in number:
    print(num)
    break
print(" ")


friends = ["Ashely", "Matt", "Michael"]
for str1 in friends[0]:
    print(str1)
print(" ")



# 2) using while

number = [1, 2, 3, 4]
i = 0
while i < len(number):
    print(number[i])
    i += 1
print(" ")


friends = ["Ashely", "Matt", "Michael"]
i = 0
while i < len(friends):
    print(friends[i])
    i += 1
